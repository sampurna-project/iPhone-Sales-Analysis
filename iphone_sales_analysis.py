data = pd.read_csv("/content/apple_products.csv")
print(data.head())

"""Before moving forward, letâ€™s have a quick look at whether this dataset contains any null values or not:

"""

print(data.isnull().sum())

"""The dataset doesnâ€™t have any null values. Now, letâ€™s have a look at the descriptive statistics of the data:"""

print(data.describe())

"""iPhone Sales Analysis in IndiaðŸ‡®ðŸ‡³

Now I will create a new dataframe by storing all the data about the top 10 highest-rated iPhones in India on Flipkart. It will help in understanding what kind of iPhones are liked the most in India:
"""

highest_rated = data.sort_values(by=["Star Rating"],
                                 ascending=False)
highest_rated = highest_rated.head(10)
print(highest_rated['Product Name'])

"""According to the above data, below are the top 5 most liked iPhones in India:



*   APPLE iPhone 11 Pro Max (Midnight Green, 64 GB)
*   APPLE iPhone 11 Pro Max (Space Grey, 64 GB)
*   APPLE iPhone 11 Pro Max (Midnight Green, 256 GB)
*   APPLE iPhone 11 Pro Max (Gold, 64 GB)
*   APPLE iPhone 11 Pro Max (Gold, 256 GB)

Now letâ€™s have a look at the number of ratings of the highest-rated iPhones on Flipkart:
"""

iphones = highest_rated["Product Name"].value_counts()
label = iphones.index
counts = highest_rated["Number Of Ratings"]
figure = px.bar(highest_rated, x=label,
                y = counts,
            title="Number of Ratings of Highest Rated iPhones")
figure.show()

"""According to the above bar graph, APPLE iPhone 8 Plus (Gold, 64 GB) has the most ratings on Flipkart. Now letâ€™s have a look at the number of reviews of the highest-rated iPhones on Flipkart:"""

iphones = highest_rated["Product Name"].value_counts()
label = iphones.index
counts = highest_rated["Number Of Reviews"]
figure = px.bar(highest_rated, x=label,
                y = counts,
            title="Number of Reviews of Highest Rated iPhones")
figure.show()

"""APPLE iPhone 8 Plus (Gold, 64 GB) is also leading in the highest number of reviews on Flipkart among the highest-rated iPhones in India. Now letâ€™s have a look at the relationship between the sale price of iPhones and their ratings on Flipkart:"""

